#문제 : 두 사람이 서로 무게가 다른 볼링공을 고르려고 한다.
# 볼링공은 총 N가가 있고, 공의 번호는 1번부터 순서대로 부여됨다. 같은 무게의 공이 여려 개 있을 수 있지만, 서로 다른 공으로 간주.
# 볼링공의 무게는 1부터 M까지의 자연수 형태로 존재




# 입력 조건 : 첫째 줄에는 볼링공 개수 N, 공의 최대 무게 M이 공백으로 구분
#            둘째 줄에는 각 볼링공의 무게 K

# 출력 조건 : 첫째 줄에 두 사람이 볼링공을 고르는 경우의 수 출력

# 입력 예시 :
# 5 3
# 1 3 2 3 2
# 출력 예시 :
# 8